#!/usr/bin/env python

class Location_Image_Info: 



	def __init__(self, x_,y_,width_,height_):
		self.x=x_
		self.y=y_
		self.width=width_
		self.height=height_

	def get_x(self):
		return self.x
	
	def get_y(self):
		return self.y

	def get_width(self):
		return self.width
	
	def get_height(self):
		return self.height






